package com.loop;

public class LoopPractice {
	public static void main(String[] args) {
		
		//(initialization/start point;condition/End point;increment/decrement)Formula for for loop
				int i;
				 for(i=0;i<=100;i++) {
					 System.out.println("this is i value" +i);		 	
		
	}
		
	int j=1;
	while(j<=100) {
		System.out.println("this is j value"+j);
		j++;
	}
	
	int k=1;
	do {System.out.println("this is k value"+k);
	   k++;}
	while(k<=100);
	
	
	
		

}

}